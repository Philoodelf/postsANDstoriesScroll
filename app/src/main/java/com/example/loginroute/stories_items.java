package com.example.loginroute;

public class stories_items {

    int story;
    String nameofstory;

    public stories_items(int story, String nameofstory) {
        this.story = story;
        this.nameofstory = nameofstory;
    }

    public int getStory() {
        return story;
    }

    public void setStory(int story) {
        this.story = story;
    }

    public String getNameofstory() {
        return nameofstory;
    }

    public void setNameofstory(String nameofstory) {
        this.nameofstory = nameofstory;
    }

}
